package Technical_Task;

public class Program {

	public static void main(String[] args) {
		
		int num=7; //series within 7 numbers
		
		for(int i=1;i<=num;i++) 
		{
			for(int j=1;j<=i;j++)
			{
				System.out.print(j);
			}
			System.out.print("*");
		}
		
		
		
		
		
		
		
	}
}
