package Technical_Task;

public class Factorialnumber {
          public static void main(String[]args) {
        	int num=2;
        	//5---> 5*4*3*2*1		
        int factorial=1;			
        	  
        for(int i=1;i<=num;i++)
        {
        	factorial=factorial*i;
        }
        	System.out.println(factorial); 
        	  
        	  
        	  
        	  
        	  
        	  
        	  
          }
}
